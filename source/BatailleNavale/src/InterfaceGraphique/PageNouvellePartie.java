     private void initGrille() {
         for(int i=0; i<=99; i++){
             final int j = i;
             jPanelTab[i] = new JPanel();
             jPanelTab[i].setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0, 0, 0)));
             jPanelTab[i].setBounds(i%10*30+400, i/10*30+170, 30, 30);
             // jPanelTab[i].setBounds(300, 300, 30, 30);
             jPanelTab[i].addMouseListener(new java.awt.event.MouseAdapter() {
                public void mouseClicked(java.awt.event.MouseEvent evt) {                        
                        jPanelMouseClicked(evt, jPanelTab[j],j);
                    }
                });
                     
             jPanelTab[i].setVisible(true);
             this.add(jPanelTab[i]);
             
             this.revalidate();
         }
        
    }
     
